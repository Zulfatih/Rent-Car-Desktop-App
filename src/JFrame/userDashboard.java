    private void button_bayar_dendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_bayar_dendaActionPerformed
        String kode_transaksi = txt_denda_kode_transaksi.getText();
        String id_user = customer_id.getText();
        String nama_penyewa = txt_denda_nama_penyewa.getText();
        String pengembalian = txt_denda_pengembalian.getText();
        String dikembalikan = txt_denda_dikembalikan.getText();
        String mobil_disewa = txt_denda_mobil_disewa.getText();
        String denda_perhari = txt_denda_perhari.getText();
        String keterlambatan = txt_denda_keterlambatan.getText();
        String total_denda = txt_denda_total_denda.getText();
        String status = "sukses";
        
        try{
            Connection konek = Koneksi.getKoneksi();
            String sql = "INSERT INTO tbl_denda (kode_transaksi, id_user, nama_penyewa, tgl_pengembalian, tgl_dikembalikan, mobil_disewa, denda_perhari, keterlambatan, total_denda, status) values(?, ?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement p = konek.prepareStatement(sql);
            p.setString(1, kode_transaksi);
            p.setString(2, id_user);
            p.setString(3, nama_penyewa);
            p.setString(4, pengembalian);
            p.setString(5, dikembalikan);
            p.setString(6, mobil_disewa);
            p.setString(7, denda_perhari);
            p.setString(8, keterlambatan);
            p.setString(9, total_denda);
            p.setString(10, status);
            p.executeUpdate();
            p.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        pengembalian_sukses();
        denda_success ds = new denda_success();
        ds.kode_denda.setText(txt_denda_kode_transaksi.getText());
        ds.nama_denda.setText(txt_denda_nama_penyewa.getText());
        ds.pengembalian_denda.setText(txt_denda_pengembalian.getText());
        ds.dikembalikan_denda.setText(txt_denda_dikembalikan.getText());
        ds.mobil_denda.setText(txt_denda_mobil_disewa.getText());
        ds.perhari_denda.setText(txt_denda_perhari.getText());
        ds.terlambat_denda.setText(txt_denda_keterlambatan.getText());
        ds.total_denda.setText(txt_denda_total_denda.getText());
        userDashboard ud = new userDashboard();
        ud.jTabbedPane1.setSelectedIndex(3);
        ud.setVisible(true);
        this.dispose();
        ds.setVisible(true);
        ds.setAlwaysOnTop(true);
    }//GEN-LAST:event_button_bayar_dendaActionPerformed

