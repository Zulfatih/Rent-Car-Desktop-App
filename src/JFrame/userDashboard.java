    private void button_submit_dataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_submit_dataActionPerformed
        String nama_penyewa = txt_nama_penyewa.getText();        
        String pilih_mobil = txt_pilih_mobil.getSelectedItem().toString();
        Date date_ambil = waktu_pengambilan.getDate(); String waktu_pengambilan = DateFormat.getDateInstance().format(date_ambil);
        Date date_balik = waktu_pengembalian.getDate(); String waktu_pengembalian = DateFormat.getDateInstance().format(date_balik);
        txt_nama_penyewa1.setText(nama_penyewa);
        txt_mobil_disewa.setText(pilih_mobil);
        txt_pengambilan.setText(waktu_pengambilan);
        txt_pengembalian.setText(waktu_pengembalian);
        
        //harga perhari 
        try{
            Connection Konek = Koneksi.getKoneksi();
            java.sql.Statement status = Konek.createStatement();
            String sql = "SELECT * FROM tbl_mobil where name='"+pilih_mobil+"'";
            ResultSet success = status.executeQuery(sql);
            while(success.next()){
                String pat = success.getString("price");
                txt_harga_perhari.setText(pat);
            }  
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Data Gagal Ditampilkan");
        }
        
        //total hari sewa
            SimpleDateFormat sdf = new SimpleDateFormat("MM/dd/yyyy", Locale.ENGLISH);
            Date pengambilan = this.waktu_pengambilan.getDate();
            Date pengembalian = this.waktu_pengembalian.getDate();
        
            long diffInMillies = Math.abs(pengembalian.getTime() - pengambilan.getTime()+1);
            long diff = TimeUnit.DAYS.convert(diffInMillies, TimeUnit.MILLISECONDS);
        
            txt_total_hari_sewa.setText(String.valueOf(diff));
        
            int total = Integer.parseInt(txt_harga_perhari.getText()) *Integer.parseInt(txt_total_hari_sewa.getText());
            String bayar= Integer.toString(total);
            txt_bayar.setText(bayar);
            txt_harga_perhari.setText(txt_harga_perhari.getText());
            txt_total_hari_sewa.setText(txt_total_hari_sewa.getText());
            
        try{
            Connection konek = Koneksi.getKoneksi();
            String sql = "SELECT MAX(id) FROM tbl_transaksi";
            PreparedStatement p = konek.prepareStatement(sql);
            ResultSet rs = p.executeQuery();
            if(rs.next()){
                int id = rs.getInt(1);
                int n = id+1;
                txt_kode_transaksi.setText(Integer.toString(n));
            }
                
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_button_submit_dataActionPerformed

